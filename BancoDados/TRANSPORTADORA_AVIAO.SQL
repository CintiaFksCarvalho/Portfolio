CREATE DATABASE TRANSPORTADORA_AVIAO;
USE TRANSPORTADORA_AVIAO;

CREATE TABLE AVIAO (
	ID INT (3) NOT NULL AUTO_INCREMENT,
	MAT VARCHAR (15) NOT NULL,
	NOME VARCHAR (50) NOT NULL,
	MARCA VARCHAR (30) NOT NULL,
	N_LUG INT (3) NOT NULL,
	AUTONOMIA INT (5) NOT NULL,
	PRIMARY KEY (ID),
	UNIQUE (MAT)
);


CREATE TABLE TELEMOVEL (
	ID INT(5) NOT NULL AUTO_INCREMENT,
	NUMERO INT(9) NOT NULL,
	PRIMARY KEY (ID),
	UNIQUE (NUMERO)
); 

CREATE TABLE MORADA (
	ID INT(4) NOT NULL AUTO_INCREMENT,
	ENDERECO VARCHAR(200) NOT NULL,
	NUM VARCHAR (10) NULL,
	COD_POSTAL VARCHAR(8) NOT NULL,
	LOCALIDADE VARCHAR(60) NOT NULL,
	PRIMARY KEY (ID) 
);

CREATE TABLE PILOTO (
	ID INT(4) NOT NULL AUTO_INCREMENT,
	BREVE VARCHAR(20) NOT NULL,
	P_NOME VARCHAR(20) NOT NULL,
	APELIDO VARCHAR(20) NOT NULL,
	DATA_NASC DATE NOT NULL,
	ID_MORADA_FK INT(4) NOT NULL,
	PRIMARY KEY (ID),
	UNIQUE (BREVE),
	FOREIGN KEY (ID_MORADA_FK) REFERENCES MORADA (ID)
);

CREATE TABLE VOO (
	ID BIGINT NOT NULL AUTO_INCREMENT,
	DATA DATE NOT NULL,
	HORA TIME NOT NULL,
	LOC_PART VARCHAR(30) NOT NULL,
	LOC_DEST VARCHAR(30) NOT NULL,
	ID_AVIAO_FK INT (3) NOT NULL,
	PRIMARY KEY (ID),
	FOREIGN KEY (ID_AVIAO_FK) REFERENCES AVIAO (ID)  
);

CREATE TABLE VOO_PILOTO (
	ID_VOO_FK BIGINT NOT NULL,
	ID_PILOTO_FK INT(4) NOT NULL,
	FOREIGN KEY(ID_VOO_FK) REFERENCES VOO(ID),
	FOREIGN KEY(ID_PILOTO_FK) REFERENCES PILOTO(ID),
	PRIMARY KEY(ID_VOO_FK, ID_PILOTO_FK)
);

CREATE TABLE PILOTO_TELEMOVEL (
	ID_TELEMOVEL_FK INT(5) NOT NULL,
	ID_PILOTO_FK INT(4) NOT NULL,
	FOREIGN KEY (ID_TELEMOVEL_FK) REFERENCES TELEMOVEL (ID),
	FOREIGN KEY (ID_PILOTO_FK) REFERENCES PILOTO (ID),
	PRIMARY KEY (ID_TELEMOVEL_FK)
);






